package in.yumtum.api.vo;

public class UserVO {
	
	private String address;
	private String city;
	private String email;
	private String fName;
	private String lName;
	private String locality;
	private String name;
	private String password;
	private String phone;
	private String restaurantsOwned;
	private Integer userId;
	
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getfName() {
		return fName;
	}
	public void setfName(String fName) {
		this.fName = fName;
	}
	public String getlName() {
		return lName;
	}
	public void setlName(String lName) {
		this.lName = lName;
	}
	public String getLocality() {
		return locality;
	}
	public void setLocality(String locality) {
		this.locality = locality;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getRestaurantsOwned() {
		return restaurantsOwned;
	}
	public void setRestaurantsOwned(String restaurantsOwned) {
		this.restaurantsOwned = restaurantsOwned;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer integer) {
		this.userId = integer;
	}
	
}
